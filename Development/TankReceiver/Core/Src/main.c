const int TANK_ID = 1; //MAKE SURE TO UPDATE FOR EVERY TANK


const int MAX_CCR = 17;
const int TIMEOUT_POLL_MILLIS = 100000;
static uint8_t tx_buf[] = {TANK_ID};
static uint8_t rx_data[4];

const int min_servo_pwm = 560;
const int max_servo_pwm = 1700;
const int servo_speed = 30;
int curr_servo_pwm = (max_servo_pwm + min_servo_pwm) / 2;

//returns a float from  -1.0 to 1.0
float normalize(int num) {
	return (num - (float)(255 / 2.0)) / (float)(255 / 2.0);
}

//TIM 4, CHNL 1 controls left motor speed
//TIM 3, CHNL 2 controls right motor speed
//PIN A9 controls left motor direction
//PIN A8 controls right motor direction
void arcade_drive(int raw_x, int raw_y) {
	float drive = normalize(raw_y) * -1;
	float rotate = normalize(raw_x);

	float maximum = fmax(fabs(drive), fabs(rotate));
	float total = drive + rotate;
	float diff = drive - rotate;

	int leftCCR = 0, rightCCR = 0, leftDir = 0, rightDir = 0;
	if (drive >= 0) {
		if (rotate >= 0) { //1st quad
			leftCCR = maximum * MAX_CCR;
			rightCCR = diff * MAX_CCR;
		} else { //2nd quad
			leftCCR = total * MAX_CCR;
			rightCCR = maximum * MAX_CCR;
		}

	} else {
		if (rotate >= 0) { //3rd quad
			leftCCR = total * MAX_CCR;
			rightCCR = maximum * -1 * MAX_CCR;
		} else { //4th quad
			leftCCR = maximum * -1 * MAX_CCR;
			rightCCR = diff * MAX_CCR;
		}

	}

	if (leftCCR < 0) {
		leftCCR *= -1;
		leftDir = 1;
	}

	if (rightCCR < 0) {
		rightCCR *= -1;
		rightDir = 1;
	}

	TIM4->CCR1 = leftCCR;
	TIM3->CCR2 = rightCCR;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_9, leftDir);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_8, rightDir);
}

void servo(uint8_t cmd) {
	//Byte 7 is L2, byte 6 is r2
	if (!(cmd & 1)) {
		curr_servo_pwm -= servo_speed;
	}

	if (!(cmd & 2)) {
		curr_servo_pwm += servo_speed;
	}

	if (curr_servo_pwm < min_servo_pwm) {
		curr_servo_pwm = min_servo_pwm;
	}
	else if (curr_servo_pwm > max_servo_pwm) {
		curr_servo_pwm = max_servo_pwm;
	}

	TIM2->CCR1 = curr_servo_pwm;
}



void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	if (rx_data[0] == TANK_ID) {
		servo(rx_data[1]);
		arcade_drive(rx_data[2], rx_data[3]);
		HAL_UART_Receive_IT(&huart6, rx_data, 4);
		HAL_UART_Transmit(&huart6, tx_buf, 1, 10);
		TIM5->CNT = 0;
	} else {
		HAL_UART_Receive_IT(&huart6, rx_data, 4);
	}

}

